   <p> Podmienky používania webovej stránky, užívateľská zmluva a zásady ochrany osobných údajov

Táto zmluva upravuje podmienky používania používateľmi materiálov a služieb webovej stránky www.spborbita.ru (ďalej len „Webová stránka“).

1. Všeobecné podmienky

1.1. Webová stránka sa šíri prostredníctvom internetu a zahŕňa informácie, materiály a údaje poskytované akciovou spoločnosťou „Hotelový komplex „Slávia““ a tretími stranami.

1.2. Táto zmluva je verejnou ponukou. Prístupom k materiálom webovej stránky sa používateľ považuje za pripojeného k tejto zmluve.

1.3. Všetka informácia uverejnená na webovej stránke (vrátane všetkých textov, fotografií, videomateriálov, zvukových nahrávok, ilustrácií, dizajnu webovej stránky) je predmetom duševného vlastníctva a chránená v súlade so zákonom Ruskej federácie o ochrane duševného vlastníctva.

1.4. Akciová spoločnosť „Hotelový komplex „Slávia““ je vlastníkom výlučných práv na uvedenú informáciu (okrem prípadov, ktoré sú osobitne uvedené v obsahu uverejnenej informácie na webovej stránke).

2. Povinnosti používateľa

2.1. Používateľ súhlasí, že nebude podnikať kroky, ktoré by mohli byť považované za porušenie ruského právneho poriadku alebo noriem medzinárodného práva, vrátane oblasti duševného vlastníctva, autorských a/alebo súvisiacich práv, ako aj akýchkoľvek krokov, ktoré vedú alebo môžu viesť k narušeniu normálnej prevádzky webovej stránky a služieb webovej stránky.

2.2. Žiadny z objektov webovej stránky nesmie byť publikovaný, kopírovaný, upravovaný, odovzdávaný alebo distribuovaný akýmkoľvek spôsobom bez predchádzajúceho písomného súhlasu akciovej spoločnosti „Hotelový komplex „Slávia““.

2.3. Používanie materiálov webovej stránky bez súhlasu držiteľa autorských práv je zakázané a ukladá zodpovednosť ustanovenú zákonom Ruskej federácie o ochrane duševného vlastníctva.

2.4. Pri citovaní materiálov webovej stránky, vrátane chránených autorských diel, je potrebný odkaz na webovú stránku (pododsek 1 bodu 1 článku 1274 Občianskeho zákonníka Ruskej federácie).

2.5. Akciová spoločnosť „Hotelový komplex „Slávia““ nerobí žiadne vyhlásenia o presnosti informácií, ktoré sú prezentované v rámci www.spborbita.ru a nenesie žiadnu zodpovednosť za presnosť takýchto informácií.

2.6. Používateľ je upozornený na to, že správa webovej stránky nenesie zodpovednosť za návštevu a používanie jej externých zdrojov, na ktoré môžu odkazy obsahovať webovú stránku.

2.7. Používateľ súhlasí s tým, že správa webovej stránky nenesie zodpovednosť a nemá priame alebo nepriame záväzky voči používateľovi v súvislosti s akýmikoľvek možnými alebo vzniknutými stratami alebo škodami súvisiacimi s akýmkoľvek obsahom webovej stránky, registráciou autorských práv a informáciami o takejto registrácii, tovarmi alebo službami, ktoré sú dostupné alebo získané prostredníctvom externých webových stránok alebo zdrojov, alebo inými kontaktmi používateľa, do ktorých vstúpil, používajúc informácie umiestnené na webovej stránke alebo odkazy na externé zdroje.

3. Ostatné podmienky

3.1. Všetky možné spory vyplývajúce z tejto zmluvy alebo súvisiace s ňou, podliehajú riešeniu v súlade s platným právnym poriadkom Ruskej federácie.

